    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        
        for(Object i : db.userDB){
            if(((loginIDField.getText().equals(((user) i).getId()))) && (((user)i).getPassword().equals(new String (loginPasswordField.getPassword())))) {
                if(i instanceof student){
                    loginScreen.setVisible(false);
                    mainStudent.setVisible(true);
                    studentLayout.show(studentContent, "studentHomePage");
                }
                else if(i instanceof staff){
                    loginScreen.setVisible(false);
                    mainStaff.setVisible(true);
                    staffLayout.show(staffContent,"staffHomePage");
                    
                }
                else if(i instanceof administration){
                    loginScreen.setVisible(false);
                    mainAdmin.setVisible(true);
                    adminLayout.show(adminContent,"adminHomePage");
                }
                loginIDField.setText("");
                loginPasswordField.setText("");
                loginErrorMessage.setText("");
                loggedUser = (user)i;
                break;
            } 
            else {
                loginErrorMessage.setText("User does not exist");
                
            }
        
        }
        
        
    }//GEN-LAST:event_loginButtonActionPerformed

